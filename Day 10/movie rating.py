rating = 4  # You can change rating between 0 to 5

if rating == 5:
    print("Excellent")
elif rating == 4:
    print("Very Good")
elif rating == 3:
    print("Good")
elif rating == 2:
    print("Average")
elif rating == 1:
    print("Poor")
elif rating == 0:
    print("Not Satisfied")
else:
    print("Invalid rating")
