mark = 88

if 90 <= mark <= 100:
    print("Grade: O")
elif 80 <= mark < 90:
    print("Grade: A+")
elif 70 <= mark < 80:
    print("Grade: A")
elif 60 <= mark < 70:
    print("Grade: B+")
elif 50 <= mark < 60:
    print("Grade: B")
elif 45 <= mark < 50:
    print("Grade: C")
else:
    print("Fail")
